package org.Hangaram.Hangaroid.AndroMoney;

public class typeParsingRule {
	long _lCardIdx;
	String _strPhoneNumber;
	String _strRegular;
	
	public typeParsingRule()
	{
		this._lCardIdx = 0;
		this._strPhoneNumber = "";
		this._strRegular = "";
	}
	
	public typeParsingRule(long CardIdx, String PhoneNumber, String regular)
	{
		this._lCardIdx = CardIdx;
		this._strPhoneNumber = PhoneNumber;
		this._strRegular = regular;
	}
	
	public long getCardIdx()
	{
		return(this._lCardIdx);
		}
	public void setCardIdx(long CardIdx)
	{
		this._lCardIdx = CardIdx;
	}
	public String getPhoneNumber()
	{
		return(this._strPhoneNumber);
	}
	public void setPhoneNumber(String PhoneNumber)
	{
		this._strPhoneNumber = PhoneNumber;
	}
	public String getRegular()
	{
		return(this._strRegular);
	}
	public void setRegular(String Regular)
	{
		this._strRegular = Regular;
	}
	
	
}

